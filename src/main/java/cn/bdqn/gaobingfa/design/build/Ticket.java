package cn.bdqn.gaobingfa.design.build;

public class Ticket {

    private int quan;
    private int youhui;
    private int ertong;
    private int laoren;

    public int getQuan() {
        return quan;
    }

    public void setQuan(int quan) {
        this.quan = quan;
    }

    public int getYouhui() {
        return youhui;
    }

    public void setYouhui(int youhui) {
        this.youhui = youhui;
    }

    public int getErtong() {
        return ertong;
    }

    public void setErtong(int ertong) {
        this.ertong = ertong;
    }

    public int getLaoren() {
        return laoren;
    }

    public void setLaoren(int laoren) {
        this.laoren = laoren;
    }
}
